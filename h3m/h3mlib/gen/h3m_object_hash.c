#define uchar unsigned char

int
h3m_object_hash(const uchar *key)
{
	int i;
	unsigned f0, f1, f2;
	const uchar *kp = key;

	for (i=-65, f0=f1=f2=0; *kp; ++kp) {
		if (*kp < 65 || *kp > 95)
			return -1;
		if (kp-key > 37)
			return -1;
		f0 += T0[i + *kp];
		f1 += T1[i + *kp];
		f2 += T2[i + *kp];
		i += 31;
	}

	if (kp-key < 4)
		return -1;

	f0 %= 53;
	f1 %= 53;
	f2 %= 53;

	if (f1 == f0 && ++f1 >= 53)
		f1 = 0;
	if (f2 == f0 && ++f2 >= 53)
		f2 = 0;
	if (f1 == f2) {
		if (++f2 >= 53)
			f2 = 0;
		if (f2 == f0 && ++f2 >= 53)
			f2 = 0;
	}

	return g[f0] + g[f1] + g[f2];
}